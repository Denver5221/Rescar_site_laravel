      <div class="col-lg-12">
        <div class="side-content left">
          <p>Le Réseau des Services de Conseil Agricole et Rural
              d’Afrique de l’Ouest et du Centre <strong>(RESCAR-AOC)</strong> émerge depuis
              <strong>2010</strong> avec la vocation d’être un des principaux acteurs d’opérationnalisation
              des politiques agricoles sous-régionales conçues pour contribuer au développement
              durable en Afrique de l’Ouest et du Centre <strong>(AOC)</strong>. L’idée de la mise en place
              du <strong>RESCAR-AOC</strong> vient des échanges entre des cadres de la recherche, du développement et de la formation agricole évoluant en AOC sur leurs activités dans le domaine du conseil agricole et rural. Ils étaient arrivés à la conclusion que le renforcement des synergies entre professionnels améliorerait
              l’efficience de leurs interventions et la contribution des services de conseil
              agricole et rural au développement de l’AOC.
          </p>
		      <p>
            Cette conclusion découlait de l’analyse de leurs expériences professionnelles
            respectives et de cinq constats : <br>
            <li>la forte dispersion, le manque de coordination
                et de cohérence dans les efforts pour redynamiser les services de conseil agricole et rural (SCAR) en AOC .</li>
            <li>la similitude des politiques, des enjeux et défis des SCAR dans les pays de l’AOC . </li>
            <li>la régionalisation accrue de l’économie, des politiques et des actions de développement .</li>
            <li>la nécessité d’améliorer l’efficience des interventions et les impacts </li>
            <li>la dynamique mondiale de concertation entre les acteurs des SCAR. </li>
          </p>
          <p>
            L’idée d’un réseau régional des acteurs et parties prenantes des SCAR s’est raffermie
            à l’occasion de la première rencontre annuelle du Forum mondial pour le conseil rural (GFRAS)
            organisée en novembre 2010 au Chili et qui avait souligné l’importance de la concertation
            comme un moyen pour relever les défis communs. Un processus participatif de déploiement s’est
            progressivement mis en place sous la tutelle de plusieurs partenaires dont le Forum Africain des services
            de conseil agricole (AFAAS) et le Forum mondial pour le conseil rural (GFRAS) entre autres.
            Ce processus bénéficie de l’appui déterminant du Conseil Ouest
            et Centre Africain pour la recherche et le développement agricoles (CORAF/WECARD).
          </p>
          <img src="{{ asset('front/images/slide03.jpg') }}" alt="Image" style="padding: 10px 0px 30px 0px;" >
          <p>
            Des avancées successives ont été réalisées grâce à l’engagement des initiateurs, à l’enthousiasme et l’énergie de nouveaux membres et
            aux encouragements et précieuses contributions des personnes ressources venant de diverses structures dont GFRAS,
            CORAF/WECARD, CIRAD, GIZ.
          </p>
          <p>
            En 2012, des échanges électroniques autour d’une note conceptuelle précisant les contours
            et les objectifs de RESCAR-AOC avaient permis aux membres de se donner une base commune et
            d’esquisser les contours et les objectifs de RESCAR-AOC. L’adhésion des acteurs institutionnels
            du développement agricole à l’idée d’un réseau régional de conseil agricole et rural est allée
            grandissante et a connu une étape décisive en 2012 avec l’organisation à Ouagadougou (Burkina Faso)
            d’un atelier interinstitutionnel d’harmonisation des stratégies pour la mise en place du RESCAR-AOC.
            Les principaux organisateurs étaient le CORAF/WECARD, AFAAS GFRAS et avec la participation du Gouvernement
            du Burkina Faso. Cet atelier avait réuni plus de cinquante personnes de 17 pays et représentants des organisations
            des producteurs, la recherche et la formation agricoles, services techniques publics, ONGs, secteur privé,
            des partenaires techniques et financiers et des médias, des fora et plateformes existantes.
            Il avait abouti à un constat partagé sur la pertinence, la plus-value et le rôle potentiel d’une structure
            comme RESCAR-AOC et sur les grandes lignes du cheminement pour sa mise en place.
          </p>
          <p>
            En février 2015, la poursuite du processus de sa mise en place de RESCAR-AOC et de la définition du champ
            et des modalités de son action a donné lieu à un atelier de formalisation organisé à Abidjan (Côte d’Ivoire)
            dans le cadre d’un partenariat entre le RESCAR-AOC, le CORAF/WECARD, AFAAS, GFRAS et l’Agence nationale pour
            le développement rural (ANADER). Cette rencontre avait permis de doter le RESCAR-AOC d’outils institutionnels
            et opérationnels appropriés pour apporter pleinement sa contribution spécifique dans la réalisation du développement
            durable en AOC. Les participants à cet atelier avaient validé les orientations stratégiques, la structuration et le
            système de gouvernance de RESCAR-AOC et convenu que son déploiement se fasse progressivement à travers une période
            transitoire de deux ans (juin 2015 – mai 2017). Cette phase d’initialisation est dédiée principalement à la
            réalisation des activités fondatrices permettant de renforcer l’engagement et la participation des membres,
            de développer des partenariats, de mettre en place le dispositif opérationnel et de faciliter l’insertion de RESCAR-AOC
            dans le paysage institutionnel de développement agricole d’AOC.
          </p>
